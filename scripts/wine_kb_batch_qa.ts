function qaToBlocks(d: QAData): any[] {
  const blocks: any[] = [
    img(), p(t('')),
    quote(b(`酒友问答 · 第 ${d.num} 期`), t(`\n${d.subtitle}`)),
    p(t('')), hr(),
  ];
  for (const item of d.items) {
    blocks.push(h2(`❓ ${item.q}`));
    blocks.push(p(b('简答：'), t(item.a)));
    blocks.push(p(t(item.detail)));
    blocks.push(p(t('')));
  }
  blocks.push(hr(), h1('💡 本期小贴士'), p(t('')), p(t(d.tip)), p(t('')), hr(), p(t('')));
  blocks.push(quote(b('酒友问答'), t(`\n有问题？欢迎在评论区留言！\n\n—— 酒友问答 · 第 ${d.num} 期 ❓`)));
  return blocks;
}

